xing =[[1,20,'水瓶座'],[2,19,'双鱼座'],[3,21,'白羊座'],[4,20,'金牛座'],[5,21,'双子座'],[6,22,'巨蟹座'],[7,23,'狮子座'],[8,23,'处女座'],[9,23,'天秤座'],[10,24,'天蝎座'],[11,23,'射手座'],[12,22,'摩羯座']]
month=int(input('请输入月分(例如：5):'))
day = int(input('请输入天数(例如：17):'))
for i in xing:
    if i[0]==month:
        if day>=i[1]:
            print('%d月%d日星座为：%s'%(month,day,i[2]))
        else:
            print('%d月%d日星座为：%s'%(month,day,xing[month-2][2]))
